
package operadorecombinadosasignación;


public class OperadoreCombinadosAsignación {


    public static void main(String[] args) {
    
        int numero =10;
        
        numero += 5;  //es igual a numero + 5
        numero -= 10;
        numero *= 10;
        numero /= 10;
        numero %= 10;
        
        
        System.out.println("el numero es : " + numero);
        
    }
    
}
