
package operadorincrementodecremento;


public class OperadorIncrementoDecremento {

  
    public static void main(String[] args) {
        
        int a = 5;
            
        a++;  // x++  +=  x + 1
        a--;  // x-- = x - 1 
        
            System.out.println();
            
        int x = 5, y;
        
        // sufijo x++  & prefijo ++x
        
        y = ++x;
        
            System.out.println(y);
            
            
       
    }
    
}
